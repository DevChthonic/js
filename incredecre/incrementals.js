// Incrementals
// let y = 2
// y = y + 5 OR y+ = 5

// Incrementing adds 1 to the operand
// If y was 8, then you do y++ then y will become 9

let hoursWorked = 0;

hoursWorked = hoursWorked + 1;
console.log(hoursWorked);

hoursWorked += 1;
console.log(hoursWorked);

hoursWorked++;
console.log(hoursWorked);
